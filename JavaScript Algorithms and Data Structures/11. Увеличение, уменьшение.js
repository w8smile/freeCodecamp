// Вы можете легко увеличить или добавить единицу к переменной с помощью ++оператора.
//
//     i++;
// является эквивалентом
//
// i = i + 1;
// Примечание. Вся строка становится i++;, что устраняет необходимость в знаке равенства.

let myVar = 87;

myVar++;//=88
// С помощью этого оператора вы можете легко уменьшить или уменьшить переменную на единицу --.
// i--;
let myVar = 11;

myVar--;//=10